#!/usr/bin/env python3
"""sol2rs — the Rust target of the extruder transpiler.

Drives the same front-end as the TypeScript target (lexer / parser /
dependency knowledge) and emits a cargo workspace:

    transpiler/rs-output/
      Cargo.toml          (workspace; generated)
      engine/             (generated crate: one .rs per transpiled .sol)
      runtime/            (hand-written crate, synced from transpiler/runtime-rs)
      differential/       (hand-written crate, synced from transpiler/differential-rs)
      ffi/                (hand-written cdylib stub, synced from transpiler/ffi-rs)

Which files are emitted is governed by ``transpiler-config-rust.json``'s
``includeFiles`` allowlist — the Rust backend is being brought up phase by
phase (value layer + pure libs first) and unlisted files would currently
emit code that fails to compile loudly rather than silently mis-simulate.
Type DISCOVERY still parses the whole source tree so signatures, struct
layouts, and constants resolve across the full codebase.

Usage:
    python3 -m transpiler src/ --target rust -o transpiler/rs-output
"""

